CREATE TABLE tiposocio (
    idtiposocio integer NOT NULL,
    nombretiposocio varchar(100) NOT NULL,
    roltiposocio varchar(50) NOT NULL,
    descripciondetiposocio varchar(200),

    CONSTRAINT tiposocio_pkey PRIMARY KEY (idtiposocio),
    CONSTRAINT uq_tiposocio_nombre UNIQUE (nombretiposocio),
    CONSTRAINT uq_tiposocio_rol UNIQUE (roltiposocio)
);

CREATE TABLE socio (
    idsocio integer NOT NULL,
    idtiposocio integer NOT NULL,
    primernombresocio varchar(40) NOT NULL,
    segundonombresocio varchar(40),
    primerapellidosocio varchar(40) NOT NULL,
    segundoapellidosocio varchar(40) NOT NULL,
    cisocio varchar(10) NOT NULL,
    fechanacimientosocio date NOT NULL,
    telefonopersonalsocio varchar(25),
    telefonotrabajosocio varchar(25),
    direcciondomiciliosocio varchar(255) NOT NULL,
    direcciontrabajosocio varchar(255),
    sexosocio varchar(1),
    estadosocio varchar(10) NOT NULL,

    CONSTRAINT socio_pkey PRIMARY KEY (idsocio),
    CONSTRAINT uq_socio_cisocio UNIQUE (cisocio),
    CONSTRAINT fk_socio_tiposocio
        FOREIGN KEY (idtiposocio)
        REFERENCES tiposocio(idtiposocio)
);

CREATE TABLE metodopago (
